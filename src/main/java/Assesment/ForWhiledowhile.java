package Assesment;

public class ForWhiledowhile {
    public static void main(String[] args) {
        System.out.print("For Loop:");
        for(int i=1;i<=5;i++){
            System.out.print(i +" ");
        }
        int j=1;
        System.out.print("While Loop:");
        while(j<=5){
            System.out.print(j +" ");
            j++;
        }
        int k=1;
        System.out.print("Do-While:");
        do{
            System.out.print(k +" ");
            k++;
        }
        while(k<=5);
    }
}
